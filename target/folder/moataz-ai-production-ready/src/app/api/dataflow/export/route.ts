import { NextResponse } from "next/server";
import { z } from "zod";
import { createZipBuffer } from "@/lib/files/zip";

export const runtime = "nodejs";

const schema = z.object({
  filename: z.string().default("dataflow-output"),
  json: z.unknown().optional(),
  jsonl: z.string().optional(),
  readme: z.string().optional()
});

export async function POST(request: Request) {
  try {
    const body = schema.parse(await request.json());
    const safe = body.filename.replace(/[^a-zA-Z0-9-_]/g, "-") || "dataflow-output";
    const files = [
      { path: `${safe}.json`, content: JSON.stringify(body.json ?? {}, null, 2) },
      { path: `${safe}.jsonl`, content: body.jsonl || "" },
      { path: `README.md`, content: body.readme || "# DataFlow Export\n\nGenerated by Moataz AI DataFlow Studio." }
    ];
    const zip = await createZipBuffer(files);
    return new Response(zip as BodyInit, {
      headers: {
        "Content-Type": "application/zip",
        "Content-Disposition": `attachment; filename="${safe}.zip"`
      }
    });
  } catch (error) {
    return NextResponse.json({ ok: false, error: error instanceof Error ? error.message : "Unknown error" }, { status: 400 });
  }
}
